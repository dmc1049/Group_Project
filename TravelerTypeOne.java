
public class TravelerTypeOne {
// used to fill a void...
	public static String RandTraveler(int Traveler) {
		int i = Traveler;
		String TravelerName = "";
		if (i == 1)
			TravelerName = "Sam";
		if (i == 2)
			TravelerName = "Irene";
		if (i == 3)
			TravelerName = "Cluad";
		if (i == 4)
			TravelerName = "Barbra";
		if (i == 5)
			TravelerName = "Jenny";
		if (i == 0)
			TravelerName = "Jody";
		return TravelerName;
	}
}
